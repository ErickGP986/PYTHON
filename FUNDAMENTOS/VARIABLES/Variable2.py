#Declaracion de Variables
Num1 = int(input("Digite un primer numero: "))
Num2 = int(input("Digite un segundo numero: "))
#Hacemos las operaciones correspondidas
suma = int(Num1 + Num2) #Convertimos a int Directamente
resta = int(Num1 - Num2)
multiplicacion = int(Num1 * Num2)
division = int(Num1 / Num2)
#Imprimimos los resultados
print("Los numeros son: ", Num1, " ", Num2)
print("La suma es: ", suma)
print("La resta es: ", resta)
print("La multiplicacion es: ", multiplicacion)
print("La division es: ", division)
