'''
Contar ocurrencias:

Dada la tupla datos = (10, 20, 10, 30, 10), cuenta cuántas veces aparece el número 10 (usa .count()).
'''
datos = (10, 20, 10, 30, 10) #creamos la tupla

aparece_10 = datos.count(10) #contamos las veces que aparece 10

print(f"los datos son: {datos}") #imprimimos la tupla
print(f"las veces que aparece 10 es: {aparece_10}") #imprimimos el contadoe de 10
