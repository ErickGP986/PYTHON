'''
Buscar índice:

En la tupla frutas = ("manzana", "banana", "naranja"), encuentra el índice de "banana" (usa .index()).
'''
frutas = ("manzana", "banana", "naranja") #creamos la tupla

buscar_banana = frutas.index("banana") #buscamos si existe banana

print(f"las frutas son: {frutas}") #imprimimos la tupla frutas
print(f"Existe banana en las frutas: {buscar_banana}") #imrimimos el indice de banana
