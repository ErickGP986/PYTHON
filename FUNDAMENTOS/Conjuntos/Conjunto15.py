'''
Intersección

Usando los sets anteriores, calcula la intersección
'''
s1 = {1, 2, 3} #creamos el primer set
s2 = {3, 4, 5} #creamos el segundo set

dos_set = s1.intersection(s2) #creamos la interseccion del primer y segundo conjunto

print(f"El primer set es: {s1}") #imprimimos el primer set
print(f"El segundo set es: {s2}") #imprimimmos le segundo set
print(f"La interseccion de los set es: {dos_set}") #imprimimmos la interseccion
