'''
Verificar pertenencia

Crea un set de caracteres: {'a', 'e', 'i', 'o', 'u'}

Verifica si 'x' está en el set.

Verifica si 'a' está en el set.
'''
caracteres = {'a', 'e', 'i', 'o', 'u'} #creamos el conjunto

if 'x' in caracteres: #vemos si x esta en el conjunto
    print(f"x si esta en caracteres {caracteres}") #impprimimimos si, si
else: #si no
    print(f"x no esta en caracteres: {caracteres}") #imprimimos si no esta

if 'a' in caracteres: #vemos si a esta en el conjunto
    print(f"a si esra en caracteres: {caracteres}") #imprimimos si esta
else: #si no
    print(f"a no esta en caracteres: {caracteres}") #imprimimos si no
