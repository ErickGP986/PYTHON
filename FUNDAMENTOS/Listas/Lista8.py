'''
Invertir una lista:

Invierte el orden de la lista ["Python", "Java", "C++"] sin usar reverse() (usa slicing).
'''
lista = ["Python", "Java", "C++"]  #creamos la lista

print(f"la lista original es: {lista}") #imprimimos la lista original
print(f"la lista en reversa es: {lista[::-1]}") #imprimimos la lista al reves con slicing
