'''
Contar ocurrencias:

Dada la lista ["a", "b", "a", "c", "a", "b"], cuenta cuántas veces aparece la letra "a" (usa el método .count()).
'''
lista = ["a", "b", "a", "c", "a", "b"] #Creamos la lista

aparece_A = lista.count("a") #contamos las veces que aparece 'a'

print(f"La lista es: {lista}") #imprimimos la lista
print(f"La letra 'a' aparece: {aparece_A} veces") #imprimimos las veces que aparece 'a'
