"""""
Mayúsculas y minúsculas:
Convierte una cadena ingresada por el usuario a:

Todo mayúsculas (.upper()).

Todo minúsculas (.lower()).

Formato título (.title()).
"""""""""
cadena = input("Ingrese una cadena de texto: ") #Le pedimos al usuario que cree la cadena de texto

print(f"cadena original: {cadena}") #Imprimimos la original
print(f"La cadena en mayusculas: {cadena.upper()}") #Imprimimos en mayusculas
print(f"La cadena en minusculas: {cadena.lower()}") #Imrpimimos en minusculas
print(f"La cadena en titulo: {cadena.title()}") #Imprimimos en titulo
